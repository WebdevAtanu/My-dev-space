    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        if(id.getText().isEmpty()||password.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter valid id and password");
        }
       
        else if(role.getSelectedIndex()==0){
        String query="select * from bookshop.user where id='"+id.getText()+"' and password='"+password.getText()+"'";
        String username=id.getText();
        try {
            con=DriverManager.getConnection("jdbc:mysql://localhost/bookshop","root","");
        } catch (SQLException ex) {
            
        }
        try {
            st=con.createStatement();
        } catch (SQLException ex) {
            
        }
        try {
            rs=st.executeQuery(query);
        } catch (SQLException ex) {
            
        }
        try {
            if(rs.next()){
                new User(username).setVisible(true);
                this.dispose();
            }
            else{
                 JOptionPane.showMessageDialog(this, "User ID/Password not Matching");
            }
        } catch (SQLException ex) {
           
        }
               
       
        }
        else
        {
            String query="select * from bookshop.admin where id='"+id.getText()+"' and password='"+password.getText()+"'";
            String adminname=id.getText();
            try {
                con=DriverManager.getConnection("jdbc:mysql://localhost/bookshop","root","");
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                st=con.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                rs=st.executeQuery(query);
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                if(rs.next()){
                    new Admin(adminname).setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Admin ID/Password not Found");
                }   } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_loginMouseClicked

